using UnityEngine;
using System;


namespace Noesis
{
    ////////////////////////////////////////////////////////////////////////////////////////////////
    public class Kernel
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////
        public static T LoadXaml<T>(string xamlFile) where T : Noesis.BaseComponent
        {
            return NoesisGUISystem.LoadXaml<T>(xamlFile);
        }

        ////////////////////////////////////////////////////////////////////////////////////////////////
        public static bool IsInitialized()
        {
            return NoesisGUISystem.IsInitialized;
        }
    }
}
